// Mapping simple des variations
const WORD_VARIATIONS: {[key: string]: string} = {
  'i': 'me',
  'am': 'me',
  'my': 'me',
  'going': 'go',
  'wanting': 'want',
  'wanted': 'want',
  'needs': 'want',
  'calling': 'call',
  'opens': 'open',
  'opening': 'open',
  'times': 'time',
  'tomorrows': 'tomorrow',
  'mornings': 'morning',
};

export function findVideoByExactMatch(word: string): string {
  const cleanWord = word.toLowerCase().replace(/[^a-z]/g, '');
  
  // Exact match
  const exactMatch = VIDEO_BANK.find(video => 
    video.replace('.mp4', '') === cleanWord
  );
  if (exactMatch) return exactMatch;
  
  // Try variations
  const variation = WORD_VARIATIONS[cleanWord];
  if (variation) {
    const variationMatch = VIDEO_BANK.find(video => 
      video.replace('.mp4', '') === variation
    );
    if (variationMatch) return variationMatch;
  }
  
  // Fallback
  return 'default.mp4';
}

